    def split_generated_text(self, combined_text):
        """–†–∞–∑–¥–µ–ª—è–µ—Ç —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –Ω–∞ Telegram –∏ –î–∑–µ–Ω —á–∞—Å—Ç–∏"""
        if not combined_text:
            logger.error("‚ùå –ü—É—Å—Ç–æ–π —Ç–µ–∫—Å—Ç –¥–ª—è —Ä–∞–∑–¥–µ–ª–µ–Ω–∏—è")
            return None, None
        
        logger.info(f"üìù –†–∞–∑–¥–µ–ª—è–µ–º —Ç–µ–∫—Å—Ç ({len(combined_text)} —Å–∏–º–≤–æ–ª–æ–≤)")
        
        # –ü—Ä–æ–±—É–µ–º —Ä–∞–∑–Ω—ã–µ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–∏
        separators = ["---", "‚Äî‚Äî", "‚Äì‚Äì‚Äì‚Äì", "***", "\n---\n", "\n----\n"]
        
        for separator in separators:
            if separator in combined_text:
                parts = combined_text.split(separator, 1)
                if len(parts) == 2:
                    tg_text = parts[0].replace("TG:", "").replace("Telegram:", "").strip()
                    zen_text = parts[1].replace("DZEN:", "").replace("–î–∑–µ–Ω:", "").strip()
                    logger.info(f"‚úÖ –ù–∞–π–¥–µ–Ω —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å '{separator}'")
                    logger.info(f"   TG —á–∞—Å—Ç—å: {len(tg_text)} —Å–∏–º–≤–æ–ª–æ–≤")
                    logger.info(f"   DZEN —á–∞—Å—Ç—å: {len(zen_text)} —Å–∏–º–≤–æ–ª–æ–≤")
                    return tg_text, zen_text
        
        logger.warning("‚ö†Ô∏è  –†–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω, –∏—Å–ø–æ–ª—å–∑—É–µ–º —ç–≤—Ä–∏—Å—Ç–∏—á–µ—Å–∫–æ–µ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ")
        
        # –ï—Å–ª–∏ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω, –ø—ã—Ç–∞–µ–º—Å—è —Ä–∞–∑–¥–µ–ª–∏—Ç—å –ø–æ –¥–ª–∏–Ω–µ
        lines = combined_text.split('\n')
        if len(lines) > 4:
            # –ò—â–µ–º –µ—Å—Ç–µ—Å—Ç–≤–µ–Ω–Ω–æ–µ –º–µ—Å—Ç–æ –¥–ª—è —Ä–∞–∑–¥–µ–ª–µ–Ω–∏—è (–ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É)
            for i in range(len(lines) - 3):
                if lines[i].strip() and not lines[i+1].strip() and lines[i+2].strip():
                    # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –º–µ–∂–¥—É –∞–±–∑–∞—Ü–∞–º–∏
                    tg_text = '\n'.join(lines[:i+1])
                    zen_text = '\n'.join(lines[i+2:])
                    logger.info(f"‚úÖ –ù–∞–π–¥–µ–Ω–æ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ –ø–æ –ø—É—Å—Ç–æ–π —Å—Ç—Ä–æ–∫–µ")
                    return tg_text[:800], zen_text[:2000]
        
        # –ü–æ—Å–ª–µ–¥–Ω–∏–π –≤–∞—Ä–∏–∞–Ω—Ç: –¥–µ–ª–∏–º –ø–æ–ø–æ–ª–∞–º
        text_length = len(combined_text)
        split_point = text_length // 2
        
        # –ò—â–µ–º –±–ª–∏–∂–∞–π—à–∏–π –∫–æ–Ω–µ—Ü –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è
        for i in range(split_point, min(split_point + 100, text_length - 1)):
            if combined_text[i] in ['.', '!', '?']:
                split_point = i + 1
                break
        
        tg_text = combined_text[:split_point].strip()
        zen_text = combined_text[split_point:].strip()
        
        logger.info(f"‚úÖ –†–∞–∑–¥–µ–ª–µ–Ω–∏–µ –ø–æ–ø–æ–ª–∞–º: TG={len(tg_text)}, DZEN={len(zen_text)}")
        return tg_text, zen_text

