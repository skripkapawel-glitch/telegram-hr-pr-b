            instruction = f"–°–æ–∫—Ä–∞—Ç–∏ —ç—Ç–æ—Ç —Ç–µ–∫—Å—Ç –¥–æ {target_max} —Å–∏–º–≤–æ–ª–æ–≤. –£–¥–∞–ª–∏ –ø–æ–≤—Ç–æ—Ä—ã, —É–ø—Ä–æ—Å—Ç–∏ —Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∫–∏, –Ω–æ —Å–æ—Ö—Ä–∞–Ω–∏ —Å–º—ã—Å–ª."
