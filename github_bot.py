    def _is_post_truncated(self, text: str) -> bool:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –æ–±—Ä–µ–∑–∞–Ω –ª–∏ –ø–æ—Å—Ç –ø–æ—Å–µ—Ä–µ–¥–∏–Ω–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è"""
        if not text:
            return False
        
        lines = [line.strip() for line in text.split('\n') if line.strip()]
        
        if not lines:
            return False
        
        # –ò—â–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é –Ω–µ-—Ö–µ—à—Ç–µ–≥ —Å—Ç—Ä–æ–∫—É
        non_hashtag_lines = [line for line in lines if not line.startswith('#')]
        
        if not non_hashtag_lines:
            return False
        
        last_line = non_hashtag_lines[-1]
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–Ω–æ—Å—Ç—å –ø–æ—Å–ª–µ–¥–Ω–µ–π —Å—Ç—Ä–æ–∫–∏
        if last_line:
            # –ï—Å–ª–∏ –ø–æ—Å–ª–µ–¥–Ω—è—è —Å—Ç—Ä–æ–∫–∞ –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ —Å–æ—é–∑ –∏–ª–∏ –ø—Ä–µ–¥–ª–æ–≥, –≤–æ–∑–º–æ–∂–Ω–æ –æ–±—Ä–µ–∑–∞–Ω–æ
            truncation_indicators = ['–∏', '–∞', '–Ω–æ', '—á—Ç–æ', '–∫–æ—Ç–æ—Ä—ã–π', '–µ—Å–ª–∏', '–∫–æ–≥–¥–∞', '—á—Ç–æ–±—ã', '–∫–∞–∫', '–≥–¥–µ']
            last_words = last_line.lower().split()[-3:]  # –ü–æ—Å–ª–µ–¥–Ω–∏–µ 3 —Å–ª–æ–≤–∞
            
            for word in last_words:
                if word in truncation_indicators:
                    return True
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–µ–∑–∞–∫—Ä—ã—Ç—ã–µ —Å–∫–æ–±–∫–∏ –∏–ª–∏ –∫–∞–≤—ã—á–∫–∏
            if '(' in last_line and ')' not in last_line:
                return True
            if '"' in last_line and last_line.count('"') % 2 != 0:
                return True
            if '¬´' in last_line and '¬ª' not in last_line:
                return True
        
        return False
    
