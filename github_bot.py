            if not question_found:
                logger.warning("‚ùå Zen –ø–æ—Å—Ç: –Ω–µ—Ç –≤–æ–ø—Ä–æ—Å–æ–≤")
                return False
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–Ω–æ—Å—Ç—å –≤–æ–ø—Ä–æ—Å–∞
            for line in lines:
                if '?' in line and not line.startswith('#'):
                    if not line.strip().endswith('?'):
                        logger.warning(f"‚ùå Zen –ø–æ—Å—Ç: –≤–æ–ø—Ä–æ—Å –Ω–µ –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –∑–Ω–∞–∫–æ–º ?")
                        return False
            
