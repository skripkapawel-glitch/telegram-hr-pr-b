    def was_slot_sent_today(self, slot_time):
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –±—ã–ª –ª–∏ —Å–ª–æ—Ç —É–∂–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω —Å–µ–≥–æ–¥–Ω—è"""
