    def format_telegram_text(self, text, slot_info):
        """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç –¥–ª—è Telegram"""
        if not text:
            return None
        
        text = text.strip()
        
        # –î–æ–±–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏ —Å–ª–æ—Ç–∞ –µ—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç
        if not text.startswith(slot_info['emoji']):
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ –∫–∞–∫–æ–π-—Ç–æ —ç–º–æ–¥–∑–∏
            emoji_pattern = re.compile("["
                u"\U0001F600-\U0001F64F"
                u"\U0001F300-\U0001F5FF"
                u"\U0001F680-\U0001F6FF"
                u"\U0001F1E0-\U0001F1FF"
                u"\U00002700-\U0001F251" 
                "]+", flags=re.UNICODE)
            
            if not emoji_pattern.match(text[:2]):
                lines = text.split('\n')
                if lines and lines[0].strip():
                    lines[0] = f"{slot_info['emoji']} {lines[0]}"
                    text = '\n'.join(lines)
        
