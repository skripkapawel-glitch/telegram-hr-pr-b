    def clean_text_for_posting(self, text):
        """–û—á–∏—â–∞–µ—Ç —Ç–µ–∫—Å—Ç –æ—Ç –∞—Ä—Ç–µ—Ñ–∞–∫—Ç–æ–≤ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è"""
        if not text:
            return ""
        
        text = re.sub(r'^\[+\s*', '', text)
        text = re.sub(r'\s*\]+$', '', text)
        text = re.sub(r'^(–í–æ—Ç|–î–µ—Ä–∂–∏|–ü–æ–∂–∞–ª—É–π—Å—Ç–∞|–°–º–æ—Ç—Ä–∏|–í–æ—Ç —Ç–µ–±–µ).+?\n', '', text, flags=re.IGNORECASE)
